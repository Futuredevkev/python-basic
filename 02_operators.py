## Operators aritmeticos ##

print(1 + 2) # Suma
print(1 - 2) # Resta
print(1 * 2) # Multiplicacion
print(1 / 2) # Division 
print(1 % 2) # Modulo (Resto de la division)
print(1 // 2) # Division aproximada a un numero entero
print(2 ** 3) # Exponente

## Operadores comparativos ## 

print(1 > 2) # Mayor
print(1 < 2) # Menor
print(1 >= 2) # Mayor o igual
print(1 <= 2) # Menor o igual
print(1 == 2) # Igual
print(1 != 2) # Diferente 

print('hola' >= 'Zola') # Compara por Ordenacion alfabetica
print(len('hola') >= len('Zola')) # Compara por longitud de la cadena
print('hola' == 'hola')
print('hola' != 'hola')
print('hola' > 'hola')
print('hola' < 'hola')
print('hola' <= 'hola')

## Operadores logicos ##
 
print(3 > 4 and "Hola" > "Python")  # and sirve para que se cumplan todas las condiciones
print(3 > 4 or "Hola" > "Python")  # or sirve para que se cumplan alguna de las condiciones
print(3 < 4 or "Hola" > "Python" and 4 == 4) 
print(not (3 > 4)) # not sirve para negar una condicion
